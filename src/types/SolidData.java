package types;

public class SolidData {
  
  public int w;
  public int h;
  public int offx;
  public int offy;
  public boolean hitType;
  
  public SolidData(int w, int h, int offx, int offy, boolean hitType) {
    
    this.w = w;
    this.h = h;
    this.offx = offx;
    this.offy = offy;
    this.hitType = hitType;
    
  }

}
